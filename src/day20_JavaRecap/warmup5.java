package day20_JavaRecap;

public class warmup5 {
	
	/*
	 5. print all lower case and upper case alphabets from the 
	 	ASCII table
	 */
	
	public static void main(String[] args) {
		
		// a~z,   A~Z
		
		for(char ch= 'a';  ch <= 'z'; ch++) {
			System.out.print(ch+" ");
		}
		
		System.out.println();
		
		for(char ch = 'A' ; ch <= 'Z'; ch++) {
			System.out.print(ch+" ");
		}
		
		
		
		
		
		
	}


}
